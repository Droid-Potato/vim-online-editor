<h2>What is the Vim Online Editor?</h2>

<p>
  This is "Vim Online", a vim editor in browser. You can install your vimrc, and this app will remember your vimrc between visits.
</p>

<p>
  The Vim Online Editor is building off groundbreaking efforts by @rhysd and @coolwanglu to bring vim to the browser.
</p>

<p>
  While those projects did a great job getting vim functionality inside the browser, there are still many missing pieces. The most important missing feature in my opinion is being able to install a vimrc to get all the normal keybindings back.
</p>

<p>
  Another really important missing feature is being able to save files easily and navigate between files easily.
</p>

<p>
  Another really important feature is being able to git clone a repo into the browser.
</p>

<p>
  What would really be cool is being able to edit files from the filesystem, using the WASI API.
</p>

<p>
  Even if direct access to the filesystem isn't possible, an autosync with the source code so that you could easily test the code would be super cool.
</p>

